#Creamos una lista llamada números y añadimos los valores
numeros = [3, 8, 6, 0, 8, 4]
print(numeros)

#Añadimos un nuevo número al final
numeros.append(7)
print(numeros)

#Eliminamos el primer número de la lista
numeros.pop(0)
print(numeros)

#Ordenamos la lista de menor a mayor
numeros.sort()
print(numeros)

#Imprimimos la lista con su longitud
print(len(numeros))