#Creamos una lista con los colores
colores = ["rojo", "verde", "azul", "amarillo"]
print(colores)

#Añadimos un color al final de la lista
colores.append("morado")
print(colores)

#Insertamos un color en la posición 2
colores.insert(2, "naranja")
print(colores)

#Eliminamos el color "verde"
colores.remove("verde")
print(colores)

#Ordenamos la lista alfaabéticamente
colores.sort()
print(colores)

#Invertimos el orden de la lista
colores.reverse()
print(colores)

